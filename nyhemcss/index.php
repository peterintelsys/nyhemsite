<div class="card-header">Tomtgränser</div>

<div class="card-content">
Vid alla tomtgränser mot gångbanorna är den yttersta markremsan samfällighetens mark (hur det ser ut för just din tomt finns beskrivet i din tomtritning). Denna remsa får användas av intilliggande fastighet om den sköts väl. Men vi ber er att tänka på att gångbanorna är smala och för er och era grannars säkerhet är det viktigt att utryckningsfordon, såsom ambulans, brandkår, polis, ska kunna ta sig fram på dessa gångbanor. För att det ska vara möjligt går det inte att alla sätter upp t ex staket, höga murar, kraftiga buskar/häckar etc precis i anslutning till gångbanorna. Det resulterar i så fall i att utryckningsfordon inte kan ta sig fram till alla fastigheter.
<br>
<br>
Ha detta i åtanke och tänk gärna ett steg längre; hur ska utryckningsfordon kunna ta sig fram till min och mina grannars fastigheter om olyckan skulle vara framme? Det är lätt att inte uppmärksamma detta förrän det är försent.
<br>
<br>
Detta innefattar även var vi placerar t ex soptunnor vid tömning på området. Diskutera med era grannar som ställer tunnorna på samma plats som ni själva. Om vi ställer tunnorna här, kan utryckningsfordon ta sig förbi och fram till våra fastigheter då? 
<br>
<br>
Fundera på vad ni själva kan göra för att underlätta och prata även med era grannar. Har ni tankar eller funderingar kring detta eller praktiska förbättringsförslag så tveka inte att kontakta styrelsen.
