<h5 id="area">Om området</h5>	
Nyhems samfällighet består av 127 friliggande villor bestående av fyra olika hustyper:
<br><br>
Typ A: 1-plansvilla på 123 kvm 
<br>
Typ B: 1 1/2 plansvilla med ”joddlarbalkong” på 156 kvm 
<br>
Typ C: 1 1/2 plansvilla på 188 kvm 
<br>
Typ D: 1 3/4 plansvilla på 115 kvm (även kallade giraffhus) 
<br><br>
Runt bostadsområdet slingrar sig Rönneå med en kommunal gång- och cykelväg emellan. Längst upp vid Mörtgränd finns en landtunga i anslutning till Rönneå som kallas ”Gåsahalsen”. ”Gåsahalsen” brukar disponeras av samfälligheten till olika aktiviteter, när intresse finns.
<br><br> 

På området har samfälligheten två lekplatser, 18 garagelängor samt parkeringsplatser. Vid Abborrgränd, finns även en stor kommunal lekplats.
<br><br>

<h5 id="loan">Lån av utrustning och möbler</h5>
Om medlemmar behöver långa stegar, en lång elkabel eller bord och stolar till festligheter etc finns detta att låna av samfälligheten. Grejerna förvaras i ett förråd i anslutning till garage nummer 12 på Abborrgränd. Nyckeln finns i ett nyckelskåp med kodlås intill förrådet. Behöver du tillgång till något i förrådet så kontakta styrelsen för att få tillgång till koden. Glöm inte att ”nolla” låset när du lagt tillbaka nyckeln efter användning. Inne i förrådet finns en lista där du skriver upp det du lånar. För att flera ska ha möjlighet till att låna bord och stolar samtidigt får man låna max 2 bord och 12 stolar per gång. 
<br><br>

<h5 id="history">Nyhems historia</h5>
Nyhems Gård är namnet på den gamla lantbruksfastighet som är grunden för området Nyhem. Gården låg i Ängelholms östra utkant och var välkänd för sina grönsaks- och jordgubbsodlingar. Ängelholms kommun köpte fastigheten och planerade villaområdet i början av 1970-talet. De första villorna var inflyttningsklara år 1976 och det tog tre år innan alla fastighetsägarna var inflyttade. På området finns en kommunal lekplats ”stora lekan”, intill Aborrgränd, där det finns några fruktträd kvar sedan tiden då Nyhems Gård hade sin köksträdgård där. Varsågod och plocka äpplen här när det är säsong!
